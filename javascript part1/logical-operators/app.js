// Example of Logical AND (&&) Operator
let marks=89;
if(marks>=90 && marks<=100){
    console.log("A+");
}
else{
    console.log("A");
}


// Example of Logical OR (||) Operator
let age=15;
if(age<18 || age>60){
    console.log("You are not eligible to work");
}    

// Example of Logical NOT (!) Operator
let isRaining=false;
if(!isRaining){
    console.log("You can go outside");
}